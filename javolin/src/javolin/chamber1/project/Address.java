package javolin.chamber1.project;

public class Address {

	private String buildingName;
	private String plotNo;
	private String appFaltNo;
	private String landMark;
	private String city;
	private String state;
	private long pincode;
	private LatAndLong latAndLong;

	public String getBuildingName() {
		return buildingName;
	}

	public void setBuildingName(String buildingName) {
		this.buildingName = buildingName;
	}

	public String getPlotNo() {
		return plotNo;
	}

	public void setPlotNo(String plotNo) {
		this.plotNo = plotNo;
	}

	public String getAppFaltNo() {
		return appFaltNo;
	}

	public void setAppFaltNo(String appFaltNo) {
		this.appFaltNo = appFaltNo;
	}

	public String getLandMark() {
		return landMark;
	}

	public void setLandMark(String landMark) {
		this.landMark = landMark;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public long getPincode() {
		return pincode;
	}

	public void setPincode(long pincode) {
		this.pincode = pincode;
	}

	public LatAndLong getLatAndLong() {
		return latAndLong;
	}

	public void setLatAndLong(LatAndLong latAndLong) {
		this.latAndLong = latAndLong;
	}
	
	

}
